        if(cmbBuilding.getSelectedItem().equals("B")){
            building = "B";
        }
        if(cmbBuilding.getSelectedItem().equals("C")){
            building = "C";
        }

        InPatient2 reg = new InPatient2(name,gender,age,phone,address,disease,status,checkin,roomt,roomn,building);
