//        else if (checkId(id)) {
//            JOptionPane.showMessageDialog(null, "Patient ID already exist!\n Please try another one.");
//        }
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter patient name");
        }
        else if(gender.equals("Select Gender...")){
            JOptionPane.showMessageDialog(null,"Please select patient gender");
        }
        else if(age.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter patient age");
        }
        else if(phone.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter patient phone number");
        }
        else if(address.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter patient address");
        }
        else if(disease.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter patient disease");
        }
        else if(status.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter the status of the diseasse");
        }
        else if(checkin.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter the date checked in");
        }
        else if(roomn.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter the room number");
        }
        else if(building.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please select building");
        }
        else if(reg.addNew())
        JOptionPane.showMessageDialog(null,"New Patient added!");
