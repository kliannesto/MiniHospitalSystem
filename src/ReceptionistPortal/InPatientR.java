    private void btnSearch2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearch2ActionPerformed
        // TODO add your handling code here:
        String id = txtSearch2.getText();
        InPatient2 ad = new InPatient2();
        ResultSet res = ad.getInfo(id);

        try {
            res.beforeFirst();
            while(res.next()){
                txtSearch2.setText(res.getString("PatientId"));
                txtName2.setText(res.getString("FullName"));

                String gender = res.getString("Gender");
                if(gender.equals("Male")){
                    cmboGender1.getModel().setSelectedItem("Male");
                }
                else if(gender.equals("Female")){
                    cmboGender1.getModel().setSelectedItem("Female");
                }

                txtAge2.setText(res.getString("Age"));
                txtPhone2.setText(res.getString("Phone"));
                txtAddress2.setText(res.getString("Address"));
                txtDisease2.setText(res.getString("Disease"));
                txtSOD2.setText(res.getString("StatusOfDisease"));
                txtDate2.setText(res.getString("CheckIn"));

                String room=res.getString("RoomType");
                if(room.equals("Ward")){
                    rdoWard2.setSelected(true);
                }
                else if(room.equals("Private")){
                    rdoPrivate2.setSelected(true);
                }

                txtRoomn2.setText(res.getString("RoomNo"));

                String building = res.getString("Building");
                if(building.equals("A")){
                    cmboBuilding1.getModel().setSelectedItem("A");
                }
                if(building.equals("B")){
                    cmboBuilding1.getModel().setSelectedItem("B");
                }
                else if(building.equals("C")){
                    cmboBuilding1.getModel().setSelectedItem("C");
                }
            }

            while(res.next()){
                txtSearch2.setText(res.getString(""));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Id does not exist!");
        }
    }//GEN-LAST:event_btnSearch2ActionPerformed

