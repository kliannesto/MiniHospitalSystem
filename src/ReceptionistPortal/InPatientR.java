    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String id = txtSearch.getText();
        InPatient2 ad = new InPatient2();
        ResultSet res = ad.getInfo(id);

        try {
            res.beforeFirst();
            while(res.next()){
                txtSearch.setText(res.getString("PatientId"));
                txtName1.setText(res.getString("FullName"));

                String gender = res.getString("Gender");
                if(gender.equals("Male")){
                    cmboGender.getModel().setSelectedItem("Male");
                }
                else if(gender.equals("Female")){
                    cmboGender.getModel().setSelectedItem("Female");
                }

                txtAge1.setText(res.getString("Age"));
                txtPhone1.setText(res.getString("Phone"));
                txtAddress1.setText(res.getString("Address"));
                txtDisease1.setText(res.getString("Disease"));
                txtSOD1.setText(res.getString("StatusOfDisease"));
                txtCheckin1.setText(res.getString("CheckIn"));

                String room=res.getString("RoomType");
                if(room.equals("Ward")){
                    rdoWard1.setSelected(true);
                }
                else if(room.equals("Private")){
                    rdoPrivate1.setSelected(true);
                }

                txtRoomn1.setText(res.getString("RoomNo"));

                String building = res.getString("Building");
                if(building.equals("A")){
                    cmboBuilding.getModel().setSelectedItem("A");
                }
                if(building.equals("B")){
                    cmboBuilding.getModel().setSelectedItem("B");
                }
                else if(building.equals("C")){
                    cmboBuilding.getModel().setSelectedItem("C");
                }
            }

            while(res.next()){
                txtSearch.setText(res.getString(""));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Id does not exist!");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

