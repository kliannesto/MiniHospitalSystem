    //function to check if the patient ID already exist
    public boolean checkId(String id) {
        PreparedStatement ps;
        ResultSet rs;
        boolean checkid = false;

        String query = "SELECT * FROM `clinical` WHERE `PatientId` =?";

        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, id);

            rs = ps.executeQuery();

            if (rs.next()) {
                checkid = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ClinicalDataR.class.getName()).log(Level.SEVERE, null, ex);
        }
        return checkid;
    }
