    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        String name = txtName1.getText();
        String height = txtHeight1.getText();
        String weight = txtWeight1.getText();
        String bp = txtBP1.getText();
        String temperature = txtTemp1.getText();
        String complaint = txtComplaint1.getText();
        String physician = txtPhysician1.getText();
        String presentillness = txtPresentIllness1.getText();
        String physical = txtPhysicalAppearance1.getText();

        Clinical2 c = new Clinical2(id,name,height,weight,bp,temperature, complaint,physician,presentillness,physical);
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter Patient id");
        }
        else if (checkId(id)) {
            JOptionPane.showMessageDialog(null, "Patient ID already exist!\n Please try another one.");
        }
        else if(name.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients name");
        }
        else if(height.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients height");
        }
        else if(weight.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients weight");
        }
        else if(bp.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients blood pressure");
        }
        else if(temperature.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients temperature");
        }
        else if(complaint.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients complaint");
        }
        else if(physician.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter physician");
        }
        else if(presentillness.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients present illness");
        }
        else if(physical.isEmpty()){
            JOptionPane.showMessageDialog(null," Please enter patients physical appearance");
        }
        else if(c.addNew()){
            JOptionPane.showMessageDialog(null," Patients vital signs added!");
        }

        btnClear1.setEnabled(true);
    }//GEN-LAST:event_btnAddActionPerformed

