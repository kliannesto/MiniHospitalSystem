    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        byte[] pic = null;
        String name = txtName.getText();
        String sex = "";
        if(rdoMale.isSelected()){
            sex="Male";
        }
        if(rdoFemale.isSelected()){
            sex="Female";
        }
        
        String age = txtAge.getText();
        String status = "";
        if(cmboStatus.getSelectedItem().equals("Single")){
            status = "Single";
        }
        if(cmboStatus.getSelectedItem().equals("Married")){
            status= "Married";
        }
        if(cmboStatus.getSelectedItem().equals("Divorced")){
            status = "Divorced";
        }
        if(cmboStatus.getSelectedItem().equals("Widow")){
            status= "Widow";
        }  
         
        String address = txtAddress.getText();
        String username = txtUsername.getText();
        String date = txtUsername.getText();
        String pass = String.valueOf(PasswordField.getPassword());
        String phone = txtPhone.getText();
        String qualification = "";
        if(cmboQualification.getSelectedItem().equals("New")){
            qualification = "New";
        }
        if(cmboQualification.getSelectedItem().equals("Pro")){
            qualification= "Pro";
        }
       
        String contract = txtContract.getText();
        
        AddReceptionist2 r = new AddReceptionist2(name,sex,age,status,address,username,pass,phone,date,qualification,contract,pic);
        /*if(id.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter receptionist id");
                }*/
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter receptionist name");
        }
        else if(sex.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter sex");
        }
        else if(age.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter age");
        }
        else if(status.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter status");
        }
        else if(address.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter address");
        }
        else if(username.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter username");
        }
        else if(pass.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter password");
        }
        else if(phone.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter phone number");
        }
        else if(qualification.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter qualification");
        }
        else if(contract.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter contract");
        }
        else if(r.addNew()) 
           JOptionPane.showMessageDialog(null,"New Receptionist added!");
    }//GEN-LAST:event_btnAddActionPerformed

