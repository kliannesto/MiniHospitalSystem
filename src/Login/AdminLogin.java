    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        PreparedStatement ps;
        ResultSet rs;
        String uname = txtUsername.getText();
        String pass = String.valueOf(PasswordField.getPassword());
        
        String query = "SELECT * FROM `admin_registration` WHERE `u_username` =? AND `u_pass` =?";
        
        try{
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, uname);
            ps.setString(2, pass);
            
            rs = ps.executeQuery();
            if(rs.next()){
                AdminPortal ap = new AdminPortal();
                ap.setVisible(true);
                ap.pack();
                ap.setLocationRelativeTo(null);
                ap.setExtendedState(JFrame.MAXIMIZED_BOTH);
                this.dispose(); 
                
                JOptionPane.showMessageDialog(null,"WELCOME TO ADMIN PORTAL"+" "+uname);
                
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Incorrect Username or Password!\n\t"+"For Admin Login only.", "Login Failed", 0);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }

        
    }//GEN-LAST:event_btnLoginActionPerformed

    
